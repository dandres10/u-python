# if elif else validando valor

condicion = 10

if condicion == True:
    print('Condicion verdadera')
elif  condicion ==  False:
    print('Condicion es falsa')
else:
    print('Condicion no reconocida')


# if ternario

condicion = 10
print('Condicion verdadera') if condicion else print('condicion es falsa')



